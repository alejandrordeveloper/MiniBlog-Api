import mongoose from "mongoose";

//Creamos el esquema para los usuarios
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

//Creamos el modelo de usuario
const User = mongoose.model("User", userSchema);

export default User;